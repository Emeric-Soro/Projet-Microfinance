        CREATE TABLE echeance_credit (
            id_echeance_credit NUMBER(19,0) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            id_credit NUMBER(19,0) NOT NULL,
            numero_echeance NUMBER(10,0) NOT NULL,
